#include<iostream>
// 입출력을 위한 기능들을 가져옵니다. (cin: 입력, cout: 출력, endl: 줄바꿈)
using std::cin; using std::cout; using std::endl;

#include<vector>
// 여러 개의 데이터를 리스트 형태로 저장하기 위해 vector(동적 배열)를 가져옵니다.
using std::vector;

#include<string>
// 글자(문자열)를 다루기 위한 기능들을 가져옵니다.
using std::string;

#include<algorithm>
// 정렬(순서대로 배치)과 같은 여러 알고리즘 기능들을 가져옵니다.
using std::sort;

// 하나의 테스트 케이스(문제 하나)를 해결하는 함수입니다.
void test() {
	int N; 
	// 정수의 개수 N을 입력받습니다.
	cin >> N;
	
	// N개의 문자열을 저장할 수 있는 'testcase'라는 이름의 상자(배열)를 만듭니다.
	// 숫자를 정수(int)가 아닌 '글자(문자열)'로 저장하는 이유는, 나중에 두 숫자를 이어 붙이기 쉽도록 하기 위해서입니다.
	// 예: 정수 10과 2를 더하면 12가 되지만, 글자 "10"과 "2"를 이어붙이면 "102"나 "210"이 됩니다.
	vector<string> testcase(N);
	
	// N번 반복하면서 상자에 숫자(문자열 형태)를 하나씩 채워 넣습니다.
	for (string& x : testcase) cin >> x;

	// sort 함수를 사용하여 상자 안의 문자열들을 특별한 규칙으로 정렬합니다.
	// 기본 정렬은 사전순(가나다순, 123순)이지만, 우리는 '가장 큰 수'를 만들어야 하므로 규칙을 직접 정해줍니다.
	sort(testcase.begin(), testcase.end(), [](const string& a, const string& b) {
		// [핵심 원리] 두 개의 문자열 a와 b가 있을 때, 앞뒤로 이어 붙여봅니다.
		// a + b (a를 앞에, b를 뒤에 붙인 결과)와 b + a (b를 앞에, a를 뒤에 붙인 결과)를 비교합니다.
		// 예: a="3", b="30" 일 때, a+b="330", b+a="303" 입니다.
		// "330"이 "303"보다 크므로, a("3")가 b("30")보다 먼저 오도록(더 높은 우선순위를 가지도록) 정렬합니다.
		return a + b > b + a;
	});

	// 만약 정렬을 다 끝냈는데 제일 앞에 있는(가장 큰) 숫자가 "0"이라면?
	// 입력된 모든 숫자가 "0"이라는 뜻입니다. (예: [0, 0, 0])
	// 이 경우 "000"이라고 출력하면 안 되고 그냥 "0" 하나만 출력해야 올바른 숫자가 됩니다.
	if (testcase[0] == "0") {
		cout << "0" << endl;
		return; // 정답을 출력했으므로 함수를 즉시 종료합니다.
	}

	// 잘 정렬된 문자열들을 순서대로 띄어쓰기 없이 쭉 이어 붙여서 출력합니다.
	// 큰 수가 앞으로 오도록 정렬해두었기 때문에 그냥 순서대로 출력하면 전체적으로 가장 큰 수가 됩니다.
	for (const string& x : testcase) cout << x;
	cout << endl; // 한 테스트 케이스의 출력이 끝났으므로 줄바꿈을 해줍니다.
}

// 프로그램이 처음 시작되는 메인 함수입니다.
int main() {
	// 아래 두 줄은 C++에서 입력과 출력을 매우 빠르게 하도록 돕는 '마법의 주문' 같은 것입니다.
	// 알고리즘 문제 풀이에서 입력값이 많을 때 시간 초과가 나지 않게 하기 위해 자주 씁니다.
	std::ios_base::sync_with_stdio(false);
	cin.tie(NULL);

	int T; 
	// 제일 처음, 테스트 케이스가 총 몇 개인지(T) 입력받습니다.
	cin >> T;
	
	// T번 반복하면서(테스트 케이스 개수만큼) 위에서 만든 test() 함수를 실행합니다.
	// (T--)는 T의 값을 하나씩 줄이면서 0이 될 때까지 반복하라는 의미입니다.
	while (T--) test();
}
